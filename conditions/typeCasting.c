#include <stdio.h>
void main() {
    int num = 10;
    printf("num = %d \n", num);
    printf("(float)num = %f \n", (float)num);

    // int / int = int 
    // float / int = float
    // int / float = float
    // float / float = float

    
    printf("1 / 2 = %d \n", num / 2);
    printf("(float)1 / 2 = %f \n", (float)num / 2);
}